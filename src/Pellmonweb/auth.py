def name_is(reqd_username):
    return lambda: reqd_username == cherrypy.request.login

# These might be handy

def any_of(*conditions):
    """Returns True if any of the conditions match"""
    def check():
        for c in conditions:
            if c():
                return True
        return False
    return check

# By default all conditions are required, but this might still be
# needed if you want to use it inside of an any_of(...) condition
def all_of(*conditions):
    """Returns True if all of the conditions match"""
    def check():
        for c in conditions:
            if not c():
                return False
        return True
    return check


# Controller to provide login and logout actions

class AuthController(object):
    
    def __init__(self, credentials):
        self.credentials = credentials
    
    def on_login(self, username):
        """Called on successful login"""
        cherrypy.log('Login from %s as username: %s'%(cherrypy.request.headers["Remote-Addr"], username))

    def on_logout(self, username):
        """Called on logout"""
        cherrypy.log('Logout from %s, username: %s'%(cherrypy.request.headers["Remote-Addr"], username))

    def get_loginform(self, username, msg="Enter login information", from_page="/"):
        from_page = escape(from_page, True)
        username = escape(username, True)
        tmpl = lookup.get_template("login.html")
        return tmpl.render(user_name=username, username=cherrypy.session.get('_cp_username'), from_page=from_page, msg=msg)

    def check_credentials(self, username, password):
        """Verifies credentials for username and password.
        Returns None on success or a string describing the error on failure"""
        # Adapt to your needs
        try:
            if (username,password) in self.credentials:
                return None
            else:
                cherrypy.log('Login failed from %s, username: %s, password: %s'%(cherrypy.request.headers["Remote-Addr"], username[:50], password[:50]))
                return "Incorrect username or password."
        except:
            cherrypy.log('Login failed from %s, username: %s, password: %s'%(cherrypy.request.headers["Remote-Addr"], username[:50], password[:50]))
            return "Incorrect username or password."

    @cherrypy.expose
    def login(self, username=None, password=None, from_page="/"):
        if username is None or password is None:
            return self.get_loginform("", from_page=from_page)
        
        error_msg = self.check_credentials(username, password)
        if error_msg:
            return self.get_loginform(username, error_msg, from_page)
        else:
            cherrypy.session[SESSION_KEY] = cherrypy.request.login = username
            self.on_login(username)
            raise cherrypy.HTTPRedirect(from_page or "/")
    
    @cherrypy.expose
    def logout(self, from_page="/"):
        sess = cherrypy.session
        username = sess.get(SESSION_KEY, None)
        sess[SESSION_KEY] = None
        if username:
            cherrypy.request.login = None
            self.on_logout(username)
        raise cherrypy.HTTPRedirect(from_page or "/")
